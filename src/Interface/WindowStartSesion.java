    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        Sesion start=new Sesion();
        if(start.expelIntruder(user.getText())){
            
            JOptionPane.showMessageDialog(null, "Bobo, no puedes entrar");
        }
        
        else
        //Comprueba lo datos de sesion  
        if(start.StartSesion(user.getText(), String.copyValueOf(pass.getPassword()))){
            
            WindowMain newWindow=new WindowMain(start);
            newWindow.setLocationRelativeTo(null);
            newWindow.setVisible(true);
            this.dispose();
        }
        
        else
        
        JOptionPane.showMessageDialog(null, "Datos Incorrectos");
    }//GEN-LAST:event_jButton2ActionPerformed

